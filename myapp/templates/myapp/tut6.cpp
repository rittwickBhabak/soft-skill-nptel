#include<iostream>
using namespace std;

int main()
{
    int a = 4, b = 5;
    cout<<"a + b = "<<a + b<<endl;
    cout<<"a - b = "<<a - b<<endl;
    cout<<"a * b = "<<a * b<<endl;
    cout<<"a / b = "<<a / b<<endl;
    cout<<"a % b = "<<a % b<<endl;
    cout<<"a + b = "<<a + b<<endl;
    cout<< "a << b = "<< (a<<b) <<endl; 
    cout<< "a >> b = "<< (a>>b) <<endl; 

    return 0;
}
// Output:
// a + b = 9
// a - b = -1
// a * b = 20
// a / b = 0
// a % b = 4
// a + b = 9
// a << b = 128
// a >> b = 0